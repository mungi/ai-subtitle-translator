import { createSubtitleDocument, parseWebVtt } from "../shared/subtitles.js";

const VIMEO_CAPTIONS_HOSTS = new Set(["captions.vimeo.com", "captions.cloud.vimeo.com"]);

export function isVimeoCaptionUrl(value) {
  try {
    const url = new URL(String(value || ""));
    return url.protocol === "https:" && VIMEO_CAPTIONS_HOSTS.has(url.hostname);
  } catch {
    return false;
  }
}

export function normalizeVimeoTextTrack(track, { videoId, playerUrl } = {}) {
  if (!track?.url || !videoId) return null;

  let url;
  try {
    url = new URL(track.url, playerUrl || "https://player.vimeo.com/");
  } catch {
    return null;
  }
  if (!isVimeoCaptionUrl(url)) return null;

  const languageCode = String(track.lang || track.language || "").trim();
  return {
    videoId: String(videoId),
    languageCode,
    label: String(track.label || track.name || languageCode || "Unknown"),
    isAutoGenerated: /(?:^|[-_])autogen(?:$|[-_])/i.test(languageCode)
      || /auto(?:matic)?/i.test(String(track.kind || track.type || "")),
    url: url.href
  };
}

function isEnglishTrack(track) {
  const language = String(track.languageCode || "").toLowerCase();
  return language === "en" || language.startsWith("en-") || language.startsWith("en_");
}

export function selectVimeoTextTrack(tracks, languageCode = "") {
  const usableTracks = Array.isArray(tracks) ? tracks.filter(Boolean) : [];
  const requestedLanguage = String(languageCode || "").toLowerCase();

  if (requestedLanguage) {
    return usableTracks.find((track) => String(track.languageCode || "").toLowerCase() === requestedLanguage && !track.isAutoGenerated)
      || usableTracks.find((track) => String(track.languageCode || "").toLowerCase() === requestedLanguage)
      || null;
  }

  if (usableTracks.length === 1) return usableTracks[0];
  return usableTracks.find((track) => isEnglishTrack(track) && !track.isAutoGenerated)
    || usableTracks.find(isEnglishTrack)
    || usableTracks[0]
    || null;
}

async function fetchText(url) {
  const response = await fetch(url, {
    redirect: "error",
    headers: {
      Accept: "text/vtt,text/plain,*/*"
    }
  });
  const text = await response.text();
  if (!response.ok) {
    throw new Error(`HTTP ${response.status}: ${response.statusText}`);
  }
  return text;
}

export async function fetchVimeoTranscript({ videoId, trackUrl, sourceLanguage, platform = "nvidia" }) {
  if (!videoId) throw new Error("Vimeo videoId is required.");
  if (!isVimeoCaptionUrl(trackUrl)) throw new Error("Vimeo caption URL is invalid.");

  const cues = parseWebVtt(await fetchText(trackUrl), { idPrefix: "vimeo" });
  return createSubtitleDocument({
    platform: platform === "vimeo" ? "vimeo" : "nvidia",
    videoId: String(videoId),
    sourceLanguage: String(sourceLanguage || "en"),
    cues
  });
}

export const vimeoCaptionInternals = {
  isEnglishTrack,
  fetchText
};
