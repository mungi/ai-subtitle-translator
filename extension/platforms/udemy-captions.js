import { createSubtitleDocument, parseWebVtt } from "../shared/subtitles.js";

const UDEMY_LECTURE_PATTERN = /\/lectures?\/(\d+)/;
const UDEMY_GRAPHQL_LECTURE_DETAILS_QUERY = `
query LectureDetails($id: ID!) {
  lecture(id: $id) {
    __typename
    ... on VideoLecture {
      asset {
        __typename
        ... on AssetVideo {
          id
          captions {
            id
            url
            language
            title
            videoLabel
            source
            status
          }
        }
      }
    }
  }
}`;

export function getUdemyLectureIdFromUrl(url) {
  const match = String(url ?? "").match(UDEMY_LECTURE_PATTERN);
  if (!match) {
    throw new Error("Could not find Udemy lecture id in URL.");
  }
  return match[1];
}

export function parseUdemyCourseData(moduleArgs, url = location.href) {
  const data = typeof moduleArgs === "string" ? JSON.parse(moduleArgs) : moduleArgs;
  const courseId = data?.courseId || data?.course_id;
  const lectureId = getUdemyLectureIdFromUrl(url);

  if (!courseId) {
    throw new Error("Could not find Udemy course id.");
  }

  return {
    courseId: String(courseId),
    lectureId: String(lectureId)
  };
}

export function readUdemyCourseDataFromDocument(documentRef = document, url = location.href) {
  const appLoader = documentRef.querySelector(".ud-app-loader");
  const moduleArgs = appLoader?.getAttribute("data-module-args");

  if (!moduleArgs) {
    throw new Error("Could not find Udemy app loader course data.");
  }

  return parseUdemyCourseData(moduleArgs, url);
}

function getUdemyOrigin(hostname) {
  if (hostname && hostname.includes(".udemy.com")) {
    return `https://${hostname}`;
  }
  return "https://www.udemy.com";
}

async function fetchJson(url, init) {
  const response = await fetch(url, init);
  const body = await response.json().catch(() => ({}));

  if (!response.ok) {
    const detail = body.detail || body.error?.message || body.message || response.statusText;
    throw new Error(`HTTP ${response.status}: ${detail}`);
  }

  return body;
}

async function fetchText(url, init) {
  const response = await fetch(url, init);
  const text = await response.text();

  if (!response.ok) {
    throw new Error(`HTTP ${response.status}: ${response.statusText}`);
  }

  return text;
}

function normalizeUdemyCaption(courseData, caption) {
  const localeId = String(caption.locale_id || caption.language || caption.locale || "").replace("_", "-");
  const languageCode = localeId.split("-")[0] || "";

  return {
    videoId: `${courseData.courseId}/lectures/${courseData.lectureId}`,
    courseId: courseData.courseId,
    lectureId: courseData.lectureId,
    languageCode,
    localeId,
    label: caption.video_label || caption.videoLabel || caption.label || localeId || "Unknown",
    isAutoGenerated: Boolean(caption.is_autogenerated || String(caption.source || "").toLowerCase() === "auto"),
    url: caption.url
  };
}

function isEnglishTrack(track) {
  const languageCode = String(track.languageCode || "").toLowerCase();
  const localeId = String(track.localeId || "").toLowerCase();
  const label = String(track.label || "").toLowerCase();

  return languageCode === "en"
    || localeId === "en"
    || localeId.startsWith("en-")
    || label.includes("english");
}

function selectCaptionTrack(tracks, { languageCode, localeId } = {}) {
  if (tracks.length === 1) return tracks[0];
  if (localeId) {
    const localeTrack = tracks.find((track) => track.localeId === localeId && !track.isAutoGenerated)
      || tracks.find((track) => track.localeId === localeId);
    if (localeTrack) return localeTrack;
  }

  if (languageCode) {
    const languageTrack = tracks.find((track) => (track.languageCode === languageCode || track.localeId === languageCode) && !track.isAutoGenerated)
      || tracks.find((track) => track.languageCode === languageCode || track.localeId === languageCode);
    if (languageTrack) return languageTrack;
  }

  return tracks.find((track) => isEnglishTrack(track) && !track.isAutoGenerated)
    || tracks.find(isEnglishTrack)
    || tracks[0];
}

export async function fetchUdemyCaptionTracks({ courseId, lectureId, hostname }) {
  if (!courseId || !lectureId) {
    throw new Error("Udemy courseId and lectureId are required.");
  }

  const courseData = {
    courseId: String(courseId),
    lectureId: String(lectureId)
  };
  return fetchUdemyCaptionTracksFromRest(courseData, hostname)
    .catch(() => fetchUdemyCaptionTracksFromGraphQL(courseData, hostname));
}

async function fetchUdemyCaptionTracksFromRest(courseData, hostname) {
  const origin = getUdemyOrigin(hostname);
  const apiUrl = `${origin}/api-2.0/users/me/subscribed-courses/${encodeURIComponent(courseData.courseId)}/lectures/${encodeURIComponent(courseData.lectureId)}/?fields[asset]=captions`;
  const body = await fetchJson(apiUrl, {
    credentials: "include",
    headers: {
      Accept: "application/json"
    }
  });
  const captions = body?.asset?.captions;

  if (!Array.isArray(captions) || captions.length === 0) {
    throw new Error("No Udemy captions are available for this lecture.");
  }

  return captions
    .filter((caption) => caption?.url)
    .map((caption) => normalizeUdemyCaption(courseData, caption));
}

async function fetchUdemyCaptionTracksFromGraphQL(courseData, hostname) {
  const origin = getUdemyOrigin(hostname);
  const body = await fetchJson(`${origin}/api/2024-01/graphql/`, {
    method: "POST",
    credentials: "include",
    headers: {
      Accept: "application/json",
      "Content-Type": "application/json"
    },
    body: JSON.stringify({
      query: UDEMY_GRAPHQL_LECTURE_DETAILS_QUERY,
      variables: {
        id: courseData.lectureId
      }
    })
  });

  if (Array.isArray(body.errors) && body.errors.length > 0) {
    throw new Error(body.errors[0]?.message || "Udemy GraphQL caption lookup failed.");
  }

  const captions = body?.data?.lecture?.asset?.captions;
  if (!Array.isArray(captions) || captions.length === 0) {
    throw new Error("No Udemy captions are available for this lecture.");
  }

  const tracks = captions
    .filter((caption) => caption?.url && String(caption.status || "SUCCESS").toUpperCase() === "SUCCESS")
    .map((caption) => normalizeUdemyCaption(courseData, caption));

  if (tracks.length === 0) {
    throw new Error("No usable Udemy captions are available for this lecture.");
  }

  return tracks;
}

export async function fetchUdemyTranscript({ courseId, lectureId, hostname, languageCode, localeId, trackUrl }) {
  const tracks = await fetchUdemyCaptionTracks({ courseId, lectureId, hostname });
  const selectedTrack = (trackUrl && tracks.find((track) => track.url === trackUrl))
    || selectCaptionTrack(tracks, { languageCode, localeId });

  if (!selectedTrack) {
    throw new Error(`No Udemy caption track found for language: ${languageCode || localeId}`);
  }

  const vtt = await fetchText(selectedTrack.url, {
    credentials: "include",
    headers: {
      Accept: "text/vtt,text/plain,*/*"
    }
  });
  const cues = parseWebVtt(vtt, { idPrefix: "udemy" });

  return createSubtitleDocument({
    platform: "udemy",
    videoId: selectedTrack.videoId,
    sourceLanguage: selectedTrack.localeId || selectedTrack.languageCode,
    cues
  });
}

