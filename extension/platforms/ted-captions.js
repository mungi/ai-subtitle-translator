import { createSubtitleDocument, parseWebVtt } from "../shared/subtitles.js";

const TED_HLS_HOST = "hls.ted.com";
const TED_MANIFEST_PATH_PATTERN = /^\/project_masters\/[^/]+\/manifest\.m3u8$/;
const TED_SUBTITLE_PLAYLIST_PATH_PATTERN = /^\/project_masters\/[^/]+\/subtitles\/([^/]+)\.m3u8$/;
const TED_CAPTION_PATH_PATTERN = /^\/project_masters\/[^/]+\/subtitles\/([^/]+)\/full\.vtt$/;

function parseUrl(value) {
  try {
    return new URL(String(value || ""));
  } catch {
    return null;
  }
}

function isTedHlsUrl(value, pathPattern) {
  const url = parseUrl(value);
  return Boolean(url
    && url.protocol === "https:"
    && url.hostname === TED_HLS_HOST
    && pathPattern.test(url.pathname));
}

export function isTedManifestUrl(value) {
  return isTedHlsUrl(value, TED_MANIFEST_PATH_PATTERN);
}

export function isTedCaptionUrl(value) {
  return isTedHlsUrl(value, TED_CAPTION_PATH_PATTERN);
}

function parseHlsAttributeList(value) {
  const attributes = {};
  const pattern = /([A-Z0-9-]+)=("(?:[^"\\]|\\.)*"|[^,]*)/gi;
  for (const match of String(value || "").matchAll(pattern)) {
    const rawValue = match[2];
    attributes[match[1].toUpperCase()] = rawValue.startsWith('"')
      ? rawValue.slice(1, -1).replace(/\\"/g, '"')
      : rawValue;
  }
  return attributes;
}

function buildTedCaptionUrl(manifestUrl, subtitlePlaylistUri) {
  let playlistUrl;
  try {
    playlistUrl = new URL(subtitlePlaylistUri, manifestUrl);
  } catch {
    return null;
  }

  if (playlistUrl.protocol !== "https:" || playlistUrl.hostname !== TED_HLS_HOST) return null;
  const match = playlistUrl.pathname.match(TED_SUBTITLE_PLAYLIST_PATH_PATTERN);
  if (!match) return null;

  playlistUrl.pathname = playlistUrl.pathname.replace(/\.m3u8$/, "/full.vtt");
  return isTedCaptionUrl(playlistUrl) ? playlistUrl.href : null;
}

export function parseTedCaptionTracks(manifest, { videoId, manifestUrl } = {}) {
  if (!videoId || !isTedManifestUrl(manifestUrl)) return [];

  return String(manifest || "")
    .replace(/\r\n?/g, "\n")
    .split("\n")
    .filter((line) => line.startsWith("#EXT-X-MEDIA:"))
    .map((line) => parseHlsAttributeList(line.slice("#EXT-X-MEDIA:".length)))
    .filter((attributes) => attributes.TYPE === "SUBTITLES" && attributes.URI)
    .map((attributes) => {
      const trackUrl = buildTedCaptionUrl(manifestUrl, attributes.URI);
      if (!trackUrl) return null;
      const languageCode = String(attributes.LANGUAGE || "").trim();
      return {
        videoId: String(videoId),
        languageCode,
        label: String(attributes.NAME || languageCode || "Unknown"),
        isAutoGenerated: false,
        trackUrl
      };
    })
    .filter(Boolean);
}

async function fetchText(url, accept) {
  const response = await fetch(url, {
    redirect: "error",
    headers: { Accept: accept }
  });
  const text = await response.text();
  if (!response.ok) {
    throw new Error(`HTTP ${response.status}: ${response.statusText}`);
  }
  return text;
}

export async function fetchTedCaptionTracks({ videoId, manifestUrl }) {
  if (!videoId) throw new Error("TED videoId is required.");
  if (!isTedManifestUrl(manifestUrl)) throw new Error("TED manifest URL is invalid.");

  const manifest = await fetchText(manifestUrl, "application/vnd.apple.mpegurl,application/x-mpegURL,text/plain,*/*");
  const tracks = parseTedCaptionTracks(manifest, { videoId, manifestUrl });
  if (tracks.length === 0) {
    throw new Error("No TED captions are available for this video.");
  }
  return tracks;
}

export async function fetchTedTranscript({ videoId, trackUrl, sourceLanguage }) {
  if (!videoId) throw new Error("TED videoId is required.");
  if (!isTedCaptionUrl(trackUrl)) throw new Error("TED caption URL is invalid.");

  const cues = parseWebVtt(await fetchText(trackUrl, "text/vtt,text/plain,*/*"), { idPrefix: "ted" });
  return createSubtitleDocument({
    platform: "ted",
    videoId: String(videoId),
    sourceLanguage: String(sourceLanguage || "en"),
    cues
  });
}

export const tedCaptionInternals = {
  buildTedCaptionUrl,
  parseHlsAttributeList,
  fetchText
};
